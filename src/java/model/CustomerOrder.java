package model;

/**
 *
 * @author ADMIN
 */
public class CustomerOrder {
    public int orderID;
    public String name;
    public String email;
    public String phone;

    public CustomerOrder() {
    }

    public CustomerOrder(int orderID, String name, String email, String phone) {
        this.orderID = orderID;
        this.name = name;
        this.email = email;
        this.phone = phone;
    }

    public int getOrderID() {
        return orderID;
    }

    public void setOrderID(int orderID) {
        this.orderID = orderID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
}
